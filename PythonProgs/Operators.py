# Operators in Python
# Arithmetic Operator
print("Arithmetic Operator")
print("5 + 6 is ", 5+6)
print("5 - 6 is ", 5-6)
print("5 * 6 is ", 5*6)
print("5 ** 3 is ", 5**3)
print("5 / 6 is ", 5/6)
print("5 % 3 is ", 5%3)
print("15 // 6 is ", 5//6)

# Assignment Operator
print("Assignment Operator")
x = 5
print(x)
x = x + 7
print(x)

# Comparision Operator
print("Comparision Operator")
i = 5
print(i >= 5)

# Logical Operator
print("Logical Operator")
a = True
b = False

print(a and b)
print(a or b)

# Identity Operator
print("Identity Operator")

print(5 is not 7)
print(5 is 7)

# Membership Operator
print("Membership Operator")

list = [3,3,2,2,39,78,67,37]
print(324 not in list)

#Bitwise Operator
print("Bitwise Operator")
# 0 - 00
# 1 - 01
# 2 - 10
# 3 - 11

print(0 & 1)
print(0 | 1)




